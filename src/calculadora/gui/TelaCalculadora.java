    private void jButton14ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton14ActionPerformed
        String valor = (String)modelo.get(1);
         
        modelo.set(0, calculadora.getMemoria());
        if(valor.equals("0"))
            return;
        else
            if (valor.charAt(0)=='-'){
            
                modelo.set(1,valor.substring(1));                                        
            }else{
                modelo.set(1,"-"+valor);
            }

    }//GEN-LAST:event_jButton14ActionPerformed

