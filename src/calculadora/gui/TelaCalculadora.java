    private void jButton16ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton16ActionPerformed

        calculadora.adicionarValorMemoria(Float.parseFloat((String)modelo.get(1)));
        try {
            
            modelo.set(1, String.valueOf(calculadora.calcular()));            
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Calculadora", JOptionPane.ERROR_MESSAGE);
            
        }finally{
            calculadora.limparMemoria();
            modelo.set(0,calculadora.getMemoria());
        }
    }//GEN-LAST:event_jButton16ActionPerformed

